def convert_string_to_upper(given_string):
  uppercase_string = given_string.upper()
  return uppercase_string

string_lower = "Prabhat"
converted_string = convert_string_to_upper(string_lower)
print(converted_string)


def convert_string_to_lower(given_string):
  lowercase_string = given_string.lower()
  return lowercase_string

string_upper = "Prabhat"
converted_string = convert_string_to_lower(string_upper)
print(converted_string)